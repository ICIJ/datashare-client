<template>
  <filter-boilerplate ref="filter" v-bind="propsWithout('hide-show-more')" hide-show-more hide-exclude
    hide-contextualize hide-sort no-infinite-scroll @reset-filter-values="resetFilterValues">
    <template #items-group>
      <b-form-checkbox-group v-model="selected" stacked class="list-group-item p-0 border-0">
        <div class="filter__items__item__wrapper px-2 mt-2">
          <b-form-checkbox :value="true" class="filter__items__item">
            <span class="filter__items__item__label px-1 text-truncate">
              {{ labelToHuman('filter.starred') }}
            </span>
            <span class="filter__items__item__count badge rounded-pill text-bg-light">
              {{ $n(starredDocuments.length) }}
            </span>
          </b-form-checkbox>
        </div>
        <div class="filter__items__item__wrapper px-2 mt-2">
          <b-form-checkbox :value="false" class="filter__items__item">
            <span class="filter__items__item__label px-1 text-truncate">
              {{ labelToHuman('filter.notStarred') }}
            </span>
            <span class="filter__items__item__count badge rounded-pill text-bg-light align-self-center">
              {{ $n(total) }}
            </span>
          </b-form-checkbox>
        </div>
      </b-form-checkbox-group>
    </template>
  </filter-boilerplate>
</template>

<script>
import get from 'lodash/get'

import { EventBus } from '@/utils/event-bus'
import FilterAbstract from '@/components/filter/types/FilterAbstract'
import FilterBoilerplate from '@/components/filter/FilterBoilerplate'
import utils from '@/mixins/utils'

/**
 * A Filter component to boolean values. Currently used for the "starred" filter but should be made generic in future versions.
 */
export default {
  name: 'FilterStarred',
  components: {
    FilterBoilerplate
  },
  extends: FilterAbstract,
  mixins: [utils],
  computed: {
    total() {
      return Math.max(get(this, '$refs.filter.total', 0) - this.starredDocuments.length, 0)
    },
    selected: {
      get() {
        return this.getFilterValuesByName(this.filter.name)
      },
      set(values) {
        this.changeYesNoValue(values)
      }
    },
    starredDocuments() {
      return this.filter.starredDocuments
    }
  },
  mounted() {
    return this.$store.dispatch('starred/fetchIndicesStarredDocuments')
  },
  methods: {
    resetFilterValues(_, refresh) {
      return this.changeYesNoValue([], refresh)
    },
    changeYesNoValue(values = [], refresh = true) {
      if (values.length === 2) {
        values = values.slice(1)
      }
      this.setFilterValue(this.filter, { key: values })
      EventBus.emit('filter::add-filter-values', { filter: this.filter, values })
      if (refresh) {
        this.refreshRouteAndSearch()
      }
    }
  }
}
</script>
